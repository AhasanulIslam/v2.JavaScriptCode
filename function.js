function sum(a, b) {
    return a + b;
}

function sub(a, b) {
    return a - b;
}

function mul(a, b) {
    return a * b;
}

function div(a, b) {
    return a / b;
}

let a = sum(5, 7);
console.log(a);

let b = sub(5, 7);
console.log(b);

let c = mul(5, 7);
console.log(c);

